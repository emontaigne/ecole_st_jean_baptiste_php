        <h3><a name="ephe">Notre Philosophie</a></h3>
        <p>Un milieu riche en découvertes où chacun profite d’un cadre rassurant, épanouissant et bienveillant.</p>
        <p>L’enfant-acteur peut :</p>
        <ul>
            <li>Vivre</li>
            <li>Exploiter sa curiosité naturelle</li>
            <li>S’intéresser aux manifestations de la vie.</li>
        </ul>

        <p>Un milieu qui :</p>
        <ul>
            <li>Suscite et développe chez l’enfant le goût, la joie et le désir d’apprendre.</li>
            <li>Développe la tolérance et l’adaptation.</li>
            <li>Sensibilise les enfants à la vie qui les entoure et aux responsabilités qu’ils ont par rapport à celle-ci.</li>
        </ul>

        <p>A l’école Anne Marie, on entre dans :</p>
        <ul>
            <li>Une école familiale, accueillante et chaleureuse où l’on cultive l’art de vivre ensemble dans le respect des différences.</li>
            <li>Une école de la réussite, ou les enseignantes se remettent sans cesse en recherche pédagogique (formations, différenciation, continuité dans les apprentissages)</li>
            <li>La pratique de pédagogies alternatives. L’enfant y est acteur de ses apprentissages.</li>
            <li>Une école où un travail d’équipe de qualité met en valeur les potentialités de l’enfant.</li>
            <li>Une école humaine pour tous où l’on accueille des enfants à besoins spécifiques et où leurs différences sont prises en compte avec un programme pour les accompagner. L’ASBL Roméo nous accompagne et nous offre leur aide.Nos différences sont une richesse ; on y pratique l’inclusion.</li>
            <li>Une école vue comme un cadre personnalisé où l’enfant est vu comme une personne qui a droit au respect à 100%.</li>
            <li>Une école qui donne aux enfants l’autonomie, le bagage pédagogique et la sécurité affective nécessaire pour l’entrée à l’école primaire choisie.</li>
        </ul>

        <p>L’éveil de l’enfant étant parmi nos priorités, nous offrons un accueil extrascolaire de qualité et varié.</p>
        <p>Cette année nous avons proposé des ateliers musicaux, des activités de massages, de jardinage et d’anglais.
