    <div class="info-onglet onglet-apropos">
        <p>L’école St Jean-Baptiste, située à Rue Armand de Moor 19, 1421 Ophain-Bois-Seigneur-Isaac, est une école maternelle et primaire. C’est une charmante école de village comptant plus ou moins 150 personnes (professeurs et élèves).</p>

        <p>L’Ecole Saint-Jean-Baptiste est une école libre et chrétienne. Elle accueille tous les enfants : croyants et non croyants, chrétiens ou fidèles à d’autres religions. Parents et enfants sont invités à partager les valeurs qui inspirent l’action de l’école, et qui font partie du bien commun de l’humanité : respect de soi et de l’autre, confiance dans les possibilités de chacun, sens du pardon, don de soi, solidarité responsable, intériorité, créativité et tolérance...</p>

        <p>Au sein de notre établissement, l'enfant-acteur peut :</p>
        <ul>
            <li>Vivre</li>
            <li>Exploiter sa curiosité naturelle</li>
            <li>S’intéresser aux manifestations de la vie.</li>
        </ul>
        <p>Il s'agit d'un milieu qui :</p>
        <ul>
            <li>Suscite et développe chez l’enfant le goût, la joie et le désir d’apprendre.</li>
            <li>Développe la tolérance et l’adaptation.</li>
            <li>Sensibilise les enfants à la vie qui les entoure et aux responsabilités qu’ils ont par rapport à celle-ci.</li>

        </ul>

        <p>Les classes des maternelles se subdivisent de la manière suivante:
        <ul>
            <li>Une classe qui reprend les élèves d’accueil et de première maternelle.</li>
            <li>Une classe qui reprend les élèves de deuxième et troisième maternelle.</li>
        </ul>

        <p>Ensuite pour l’école primaire, tous les élèves sont regroupés en trois classes distinctes:</p>
        <ul>
            <li>Les élèves de première et deuxième constituent une classe.</li>
            <li>Les élèves de troisième et quatrième une deuxième classe.</li>
            <li> Et enfin les élèves de cinquième et sixième la dernière classe de l’établissement</li>
        </ul>

        <p>L'école emploie</p>
        <ul>
            <li>5 professeurs enseignants</li>
            <li>1 professeur de gymnastique</li>

        </ul>
