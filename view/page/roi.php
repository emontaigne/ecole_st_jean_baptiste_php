    <h3>Projet d'établissement.</h3>
    <p>Le projet d'établissement définit l'ensemble des choix pédagogiques et des actions concrètes particulières que l'équipe éducative de l'établissement entend mettre en oeuvre en collaboration avec l'ensemble des acteurs et partenaires pour réaliser les projets éducatif et pédagogique du PO.</p>
    <p>Le projet d'établissement est un outil pour atteindre les objectifs généraux particuliers du décret "Mission" du 24 juillet 1997.</p>

    <p>A l'école Saint Jean-Baptiste, nous voulons partir à la découverte de la nature par les différentes approches pédagogiques reprises dans le programme d'éveil. Celui-ci nous parle "d'initiation scientifique".</p>

    <p>Nous accompagnerons les élèves dans une pédagogie active pour les amener à devenir des êtres responsables. Les activités viseront la curiosité, l'émerveillement, tout en utilisant la demarche scientifique.
      Concrètement, voici ce que nous allons mettre en place:</p>

    <ol>
      <li>La construction d'une "charte écologique" ayant pour but de sensibiliser les élèves au respect de l'environnement par:</li>
      <ul>
        <li>Le tri des déchets</li>
        <li>La réflexion autour de la consommation de l'eau</li>
        <li>La mise en place de collation entraînant moins de déchets</li>
        <li>Des visites et exposés autour du thème (visite d'un centre de tri, d'une station d'épuration)</li>
      </ul>

      <li>L'aménagement du jardin:</li>
      <ul>
        <li>La réalisation d'un potager qui implique la mise en route, les semis, l'entretien, la consommation et la vente de la production...</li>
        <li>L'installation d'une station météo</li>
        <li>La réalisation d'un compostage : poubelle verte</li>
        <li>La fabrication de mangeoires et de nichoirs</li>
        <li>L'entretien d'un poulailler</li>
      </ul>
      <li>Découverte des êtres vivants:</li>
      <ul>
        <li>Observation du cycle de la vie de la coccinelle et du papillon</li>
        <li>Observation d'une fourmillière</li>
        <li>Découverte des abeilles</li>
      </ul>

      <li>Balade dans le quartier:</li>
      <p>La situation de l'école nous permet de découvrir les différent commerces et institutions du village (boulangerie, magasin, pharmacie, boucherie, coiffeur, ferme...) mais aussi de se rendre dans le bois à plusieurs petits étangs (marres) accueillant canards, grenouilles...et de découvrir celui-ci lors des différentes saisons.</p>

      <li>Visites et exposés autour du sujet</li>

      <li>L'arbre mon ami</li>
      <p>Qu'il trône dans le jardin, au coin de la rue, ou entouré d'autres au parc ou en forêt, l'arbre vit et est un point d'ancrage important dans les relations de l'homme à la nature.</p>
      <p>Les quelques activités que nous proposons permettron de découvrir l'arbre au fil des saisons, de rentrer en contact avec lui, d'éveiller ses sens, de le comprendre...mais aussi, et surtout, d'apprendre à le regarder tout simplement.</p>

      <p>Tous les exemples cités précédemment le sont à titre indicatif et sont susceptibles d'être modifiés ou enrichis en fonction des projets de l'année.
        Les enfants auront ainsi l'occasion d'explorer un maximum de ces activités au cours de leur scolarité dans notre établissement.</p>

      <p>Nous leur preparons les milles joies de la découverte!</p>

      <p><em>"Le commencement de toutes les sciences, c'est l'étonnement de ce que les choses sont ce qu'elles sont" (Aristote)</em></p>
    </ol>
