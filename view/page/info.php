  <div class="info-onglet onglet-apropos">
    <h3><a name="">Apropos</a></h3>
    <p>Notre projet d’établissement définit l’ensemble
      de nos choix pédagogiques et de nos actions concrètes particulières
      que notre équipe éducative entend mettre en œuvre en collaboration
      avec l’ensemble des acteurs et partenaires pour réaliser le projet éducatif et pédagogique de l’école.
    </p>
    <ol>
      <h3>La vie au quotidien</h3>
      <li class="infoTitre">L'HORAIRE</li>
      <ul>
        <li>L'école est ouverte de 7h à 18h tous les lundis, mardis, jeudis et vendredis et de 7h à 13h le mercredi.</li>
        <li>Début des cours: 8h45 tous les jours pour toutes les classes</li>
        <li>Pause de midi: de 12h00 à 12h40: repas pour les maternelles de 12h40 à 13h30: sieste ou récréation en maternelle de 12h20 à 12h40: repas pour les primaires de 12h40 à 13h30: récréation pour les primaires</li>
        <li>Fin des cours: 15h10 les lundis, mardis, jeudis et vendredis 12h20 les mercredis en maternelles et primaires La cour est surveillée à partir de 8h30. Les enfants qui arrivent plus tôt doivent se rendre à la garderie ; ils ne peuvent rester sans surveillance dans la cour.</li>

      </ul>

      <li class="infoTitre">L’ACCUEIL EXTRASCOLAIRE</li>
      <ul>
        <li>L’accueil du matin: accueille les enfants de 7h à 8h30 et fonctionne tous les jours où l'école est ouverte.</li>
        <li>L’accueil du soir: accueille les enfants de 15h30 à 18h.</li>
        <li>L’accueil du mercredi après-midi: les enfants apportent leur pique-nique + une boisson et mangent à l'école. La garderie à Saint-Jean-Baptiste se fait jusque maximum 13h00.</li>
      </ul>
      <li class="infoTitre">L’ÉTUDE DIRIGÉE</li>
      <ul>
        <li class="infoTitre">Cette étude est organisée par une asbl extérieure (Educ&Sport) à l’école de 15h30 à 16h30 et assurée par une ou plusieurs enseignante(s). Les informations sont diffusées directement par les institutrices. Attention, il n’y a plus d’étude les lundis.</li>
      </ul>

      <li class="infoTitre">LE COURS DE GYMNASTIQUE</li>
      <ul>
        <li>Pour le cours de gymnastique, nous demandons que les élèves des classes primaires portent la tenue suivante: - des sandales blanches de gymnastique (si possible à élastique pour le degré inférieur) - un short bleu (les filles peuvent porter un collant bleu) - un T-shirt de l'école (proposé à la vente à l'école via M. Christian ABBISS)</li>

      </ul>

      <li class="infoTitre">LES COURS DE NATATION</li>
      <ul>
        <li>Les cours de natation seront donnés les mardis après-midi aux élèves de la 2ème maternelle à la 6 ème primaire. En primaire, ce cours remplacera le cours de gymnastique à raison de +/- 11 séances par an.</li>
        <li>En pratique : Trimestre 1 : P5/6 Trimestre 2 : P3/4 32 Trimestre 3 : P1/2 En maternelle : Semestre 1 : M3 (+/-16 séances) Semestre 2 : M2 (+/-16 séances)</li>
      </ul>

      <li class="infoTitre">LES COLLATIONS</li>
      <ul>
        <li>L’enfant apportera :
        <li>Le lundi : un fruit</li>
        <li>Le mardi : une collation « coup de cœur » au choix</li>
        <li>Le mercredi : un fruit</li>
        <li>Le jeudi : un produit laitier</li>
        <li>Le vendredi : un biscuit</li>
      </ul>

      <li class="infoTitre">LA FRÉQUENTATION SCOLAIRE</li>
      <ul>
        <li>Nous insistons beaucoup sur la régularité de la fréquentation scolaire. Toute absence ou tout retard est en effet d’abord préjudiciable à l’enfant.</li>
        <li>La loi n’autorise que certaines absences qui sont reprise dans la circulaire 3628. Ces dispositions sont explicitées dans le chapitre 4.2.2 du ROI.</li>
      </ul>

      <li class="infoTitre">L'ACCUEIL DU TEMPS DE MIDI</li>
      <ul>
        <li>Afin d'assurer un meilleur accueil et un meilleur service aux élèves restant à l'école de 12h à 13h30, nous devons faire appel à du personnel extérieur et assurer l'achat et l'entretien du matériel.</li>
        <li>Ces frais ne sont couverts par aucune subvention de la Communauté française. C'est pourquoi une participation financière est demandée à tous les élèves qui restent à l'école pendant le temps de midi, aussi bien en maternelle qu'en primaire, que ce soit pour le "dîner tartines" ou les repas chauds.</li>

      </ul>

      <li class="infoTitre">LES CLASSES DE MER :</li>
      <ul>
        <li>Une année sur deux (pas cette année-ci), les élèves de la classe d’accueil, de 1ère , 2 ème et 3ème maternelles, partent 3 jours en classes de mer.</li>
        <li>Le prochain voyage se déroulera au Centre Horizon à Bredene du 14 au 16 juin 2021. Une épargne sera proposée pendant les 3 mois qui précèderont le voyage.</li>
      </ul>

      <li class="infoTitre">LES CLASSES VERTES :</li>
      <ul>
        <li>Une année sur deux (pas cette année-ci), les élèves de 1ère, 2ème , 3 ème et 4ème primaires partent 5 jours en classes vertes.</li>
        <li>Le prochain voyage se déroulera à Wellin du 15 au 19 mars 2021. Une épargne sera proposée pendant les mois qui précèderont le voyage.</li>
        <li>N.B. : Ceci est un choix pédagogique. Nous sommes convaincus qu’il s’agit d’expériences de vie uniques. Cette activité est donc obligatoire.</li>
      </ul>

      <li class="infoTitre">LES CLASSES DE NEIGE :</li>
      <ul>
        <li>Une année sur deux (pas cette année-ci), les élèves de 5ème et 6ème primaires partiront 8 jours en classes de neige.</li>
        <li>Le prochain voyage aura lieu dans le courant du 2ème trimestre de l’année scolaire 2020-2021. Une épargne sera proposée à partir de la 4 ème primaire.</li>
        <li>N.B. : Ceci est un choix pédagogique. Nous sommes convaincus qu’il s’agit d’expériences de vie uniques. Cette activité est donc obligatoire.</li>
      </ul>

      <li class="infoTitre">PARASCOLAIRE</li>
      <ul>
        <li>Toute l’offre extra-scolaire sera disponible en version électronique via le site de la Commune de Braine-l'Alleud et en version papier via le livre aux enfants. Celui-ci sera distribué à la rentrée à toutes les familles.</li>
        <li>Certaines offres seront affichées durant l’année et les flyers seront alors disponibles aux valves.</li>
        <li>L'Académie de musique se déplace à l'école St Jean-Baptiste, des informations complémentaires peuvent être obtenues lors de la réunion de rentrée ou directement via l'Académie.</li>
      </ul>
    </ol>
