    <h3><a name="acti">Le service traiteur</a></h3>
        <p class="para-service-traiteur">
        Depuis plusieurs années, un service traiteur offre le service de repas chauds aux élèves et au personnel de l'école.Vous avez la possibilité de visualier en ligne le menu des repas pour votre enfant dans la section privée.
           <br>
           Les repas sont élaborés par des techniciennes en diététiques. Ils respectent tous les éléments essentiels recommandé pour un bon développement et une bonne santé et sont approuvées par des nutritionnistes chevronnées.</p>
           <p class="para-service-traiteur">
           Chaque jour de classe, une dizaine d’employés se déploient dans les cuisines, les salles de repas ainsi que sur la route afin que plus jamais, étudier le ventre vide ne soit une option.
            </p>

            <p class="para-service-traiteur">
            Chaque élève reçoit tous les éléments essentiels pour un bon développement physique et intellectuel. Nous prenons une attention particulière afin d’inclure de nouveaux aliments sains et riches en nutriments. Tout est pensé de façon à faire prendre conscience aux élèves l’importance de prendre de bonnes habitudes alimentaires.
            </p>
